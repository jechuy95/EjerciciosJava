package ejerciciospractica;

/**
 *
 * @author Jesus Rojas
 *  Ejemplo de uso de for. Programa Java que muestre los números del 1 al 100 utilizando la 
instrucción for.
 */
public class ForUnoCien {
    public static void main(String[] args) {
        for (int i = 1; i <=100; i++) {
            System.out.println("El numero con el ciclo for es : " + i);
        }
    }
}
