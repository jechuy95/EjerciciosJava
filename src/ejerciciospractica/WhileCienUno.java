package ejerciciospractica;

/**
 *
 * @author Jesus Rojas
 *  Ejemplo de uso de while. Programa Java que muestre los números del 100 al 1 utilizando 
la instrucción while.
 */
public class WhileCienUno {
    public static void main(String[] args) {
        int x= 100;
        while (x>=1) {            
            System.out.println("El numero es : " + x);
            x--;
        }
    }
}
