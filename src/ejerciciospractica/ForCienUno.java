package ejerciciospractica;

/**
 *
 * @author Jesus Rojas
 *  Programa Java que muestre los números del 100 al 1 utilizando la 
instrucción for.
 */
public class ForCienUno {
    public static void main(String[] args) {
         for (int i = 100; i >= 1; i--) {
             System.out.println("numero:" + i);
        }
    }
}
